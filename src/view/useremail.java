    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
String email = jTextField3.getText().trim();
        String otp = jTextField4.getText().trim();
        
        if (email.isEmpty() || otp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter email and OTP");
            return;
        }
        
        // Verify OTP
        if (UserDAO.verifyOtp(email, otp)) {
            JOptionPane.showMessageDialog(this, "OTP Verified Successfully!");
            
            // Open reset password form
            ResetPassword resetForm = new ResetPassword(email);
            resetForm.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid or expired OTP");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

