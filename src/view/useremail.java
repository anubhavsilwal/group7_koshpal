    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       String email = jTextField3.getText().trim();
        
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter email first");
            return;
        }
        
        // Check if user exists
        if (!UserDAO.userExists(email)) {
            JOptionPane.showMessageDialog(this, "Email not found in system");
            return;
        }
        
        // Generate OTP
        String otp = OTPUtil.generateOTP();
        
        // Save OTP to database
        boolean saved = UserDAO.saveOtp(email, otp);
        
        if (saved) {
            // Show OTP (in real app, send via email)
            JOptionPane.showMessageDialog(this,
                "OTP Generated!\n" +
                "OTP: " + otp + "\n" +
                "(For testing - in real app this would be emailed)",
                "OTP Sent",
                JOptionPane.INFORMATION_MESSAGE);
            
            // Enable OTP field
            jTextField4.setEnabled(true);
            jTextField4.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Error generating OTP");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

